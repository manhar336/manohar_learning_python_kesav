#abs()    #absolute value of x and  positive distance between x and 0.
print(abs(10.67))
print(abs(-10.75))  #it will make it as positive value
#ceil()   #it will take smallest integer value like 25.67 means 25
import math
print(math.ceil(26.01))  #This method returns smallest integer not less than x.
print(math.floor(26.71))  #The floor of x: the largest integer not greater than x
#exponnent value  The exponential of x: ex
print(math.exp(10))  #exponent value of 10
print(math.exp(-10)) #exponent value of -10
#find out PI value
print(math.pi)
#fabs method (similar to abs method
print(math.fabs(10.2))
print(math.fabs(-10.25))
#log() method
print(math.log(10))
#modf
print(math.modf(10.123))  #it will seperate as 2 numbers like 0.1229 and 10
print(math.modf(10))  #it will seperate as 0.0 and 10.0
print(math.modf(math.pi))
#pow()
print(math.pow(2,3))  #2 tothe power of 3
#round()
print(round(10.222,2))   #it will round off and then print 2 no's
#sqrt
print(math.sqrt(25))     #It will take square root of 25